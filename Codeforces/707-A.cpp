#include<bits/stdc++.h>
using namespace std;
int main(){
    int n, m;
    cin >> n >> m;
    char c;
    int r = 0;
    for(int i = 0; i < n; i++)
        for(int j = 0; j < m; j++){
            cin >> c;
            if(c != 'B' && c != 'W' && c != 'G')
                r = 1;
        }
    
    if(r)
        cout << "#Color" << endl;
    else
        cout << "#Black&White" << endl;
}#include<bits/stdc++.h>
