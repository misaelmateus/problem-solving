#include <cstdio>
#include <algorithm>

using namespace std;

int a, b, c, d;

int main() {

    scanf("%d %d %d %d", &a, &b, &c, &d);

    int ans = max(2, abs(a - c) + 1) + max(2, abs(b - d) + 1);

    printf("%d\n", 2 * ans);

    return 0;

}
