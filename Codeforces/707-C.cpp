#include<bits/stdc++.h>
#define ll long long 
using namespace std;
int main(){
    ll n;
    cin >> n;
    if(n <= 2LL){
        cout << -1 << endl;
        return 0;
    }
    
    ll c, b;
    if(n % 2 == 0LL){
        c = 1LL + (n*n)/4LL;
        b = (n*n)/4LL - 1LL;
    }
    else{
        c = (1LL + n*n)/2LL;
        b = (n*n - 1)/2LL;
    }
    
    cout << b << " " << c << endl;
}#include<bits/stdc++.h>
