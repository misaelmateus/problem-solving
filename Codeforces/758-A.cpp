#include<bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    cin >> n;
    int v;
    int ma = 0, sum = 0;
    for(int i = 0; i < n; i++)
    {
        cin >> v;
        if(v > ma)
            ma = v;
        sum += v;
    }
    
    cout << ma*n - sum << endl;
}#include<bits/stdc++.h>
